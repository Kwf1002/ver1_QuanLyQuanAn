    public void laydulieu_nuocuong(ArrayList<NuocUong> dsnu) {
        tblData_NuocUong.removeAllElements();
        for (NuocUong b : dsnu) {
            Vector v = new Vector();
            v.add(b.getMaDoUong());
            v.add(b.getTenDoUong());
            v.add(b.getGiaTien());

            tblData_NuocUong.add(v);
        }
        table_nuocuong.setModel(new DefaultTableModel(tblData_NuocUong, tblTitle_NuocUong));
    }

