    private void table_nuocuongMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_nuocuongMouseClicked
        // TODO add your handling code here:
        int selectedRow = table_nuocuong.getSelectedRow();

        if (selectedRow != -1) {
            // Lấy dữ liệu từ hàng được chọn
            String madouong = (String) table_nuocuong.getValueAt(selectedRow, 0);
            String tendouong = (String) table_nuocuong.getValueAt(selectedRow, 1);
            String giatien = table_nuocuong.getValueAt(selectedRow, 2).toString();

            // Hiển thị thông tin trong các trường hoặc thực hiện các hành động khác ở đây
            // Ví dụ: hiển thị thông tin trong các JTextField
            txtMaDoUong.setText(madouong);
            txtTenDoUong.setText(tendouong);
            txtGiaTienDU.setText(giatien);
        }
    }//GEN-LAST:event_table_nuocuongMouseClicked

    private void btnRefreshNuocUongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRefreshNuocUongActionPerformed
        // TODO add your handling code here:
        ArrayList<NuocUong> newDsnu = NuocUong_DAO.LayThongTinNuocUong();

        // Xoá nội dung của các JTextField
        txtMaDoUong.setText("");
        txtTenDoUong.setText("");
        txtGiaTienDU.setText("");
        
        // Cập nhật danh sách nước uống
        dsnu.clear();
        dsnu.addAll(newDsnu);

        // Làm mới bảng hiển thị
        laydulieu_nuocuong(dsnu);
  
        // Hiển thị thông báo cho người dùng
        JOptionPane.showMessageDialog(this, "Danh sách nước uống và các trường đã được làm mới !", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnRefreshNuocUongActionPerformed

