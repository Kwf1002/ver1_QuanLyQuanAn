    public void laydulieu_monan(ArrayList<MonAn> dsma) {
        tblData_MonAn.removeAllElements();
        for (MonAn b : dsma) {
            Vector v = new Vector();
            v.add(b.getMaMon());
            v.add(b.getTenMon());
            v.add(b.getGiaTien());

            tblData_MonAn.add(v);
        }
        table_monan.setModel(new DefaultTableModel(tblData_MonAn, tblTitle_MonAn));
    }

