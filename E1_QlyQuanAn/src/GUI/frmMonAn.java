    private void table_monanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_monanMouseClicked
        // TODO add your handling code here:
        // Lấy dòng (row) được chọn bằng cách sử dụng phương thức getSelectedRow
        int selectedRow = table_monan.getSelectedRow();

        if (selectedRow != -1) {
            // Lấy dữ liệu từ hàng được chọn
            String mamon = (String) table_monan.getValueAt(selectedRow, 0);
            String tenmon = (String) table_monan.getValueAt(selectedRow, 1);
            String giatien = table_monan.getValueAt(selectedRow, 2).toString();

            // Hiển thị thông tin trong các trường hoặc thực hiện các hành động khác ở đây
            // Ví dụ: hiển thị thông tin trong các JTextField
            txtMaMon.setText(mamon);
            txtTenMon.setText(tenmon);
            txtGiaTienMA.setText(giatien);
        }
    }//GEN-LAST:event_table_monanMouseClicked

