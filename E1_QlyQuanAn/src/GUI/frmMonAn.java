    private void btnThemMAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemMAActionPerformed
        // TODO add your handling code here:
        // Lấy thông tin từ các trường nhập liệu
        String mamon = txtMaMon.getText();
        String tenmon = txtTenMon.getText();
        String giatienStr = txtGiaTienMA.getText();

        // Kiểm tra tính hợp lệ của giá tiền
        double giatien = 0;
        try {
            giatien = Double.parseDouble(giatienStr);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Giá tiền không hợp lệ. Vui lòng nhập số.", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            ConnectionDB conn = new ConnectionDB();
            conn.GetConnect();
            Connection connection = conn.GetConnect();

            // Tạo một CallableStatement để gọi thủ tục ThemMonAn
            String sql = "{ call ThemMonAn(?, ?, ?) }";
            CallableStatement callableStatement = connection.prepareCall(sql);

            // Đặt các tham số cho thủ tục
            callableStatement.setString(1, mamon);
            callableStatement.setString(2, tenmon);
            callableStatement.setDouble(3, giatien);

            // Thực hiện thủ tục
            callableStatement.execute();

            JOptionPane.showMessageDialog(this, "Thêm món ăn thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);

            // Sau khi thêm thành công, làm mới dữ liệu và xoá nội dung của các trường nhập liệu
            btnRefreshDoAnActionPerformed(evt);
            txtMaMon.setText("");
            txtTenMon.setText("");
            txtGiaTienMA.setText("");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Lỗi khi thêm món ăn: " + ex.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnThemMAActionPerformed

